public class LogicalOperatorExample {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;

        System.out.println("a adalah " + a);
        System.out.println("b adalah " + b);
        System.out.println("-----------");

        System.out.print("a && b --> ");
        System.out.println(a && b);

        System.out.print("a || b --> ");
        System.out.println(a || b);

        System.out.print("a != b --> ");
        System.out.println(a != b);
    }
}
