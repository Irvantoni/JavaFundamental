public class ExampleComparisonOperator {

    public static void main(String[] args) {
        int a = 24;
        int b = 29;

        System.out.println("a = " + a);
        System.out.println("b = " + b);
        
        System.out.println("~~~~~~~~~");

        System.out.print("a == b --> ");
        System.out.println(a == b);

        System.out.print("a != b --> ");
        System.out.println(a != b);

        System.out.print("a < b --> ");
        System.out.println(a < b);

        System.out.print("a > b --> ");
        System.out.println(a > b);

        System.out.print("a <= b --> ");
        System.out.println(a <= b);

        System.out.print("a >= b --> ");
        System.out.println(a >= b);
        
    }
}